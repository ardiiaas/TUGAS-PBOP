class titik:
    def __init__(self,x,y):
        self.x = x
        self.y = y

titik_a = titik(0,0)
titik_b = titik(3,4)

print("Titik A memiliki koordinat ({},{}) ".format(titik_a.x,titik_a.y))
print("Titik B memiliki koordinat ({},{}) ".format(titik_b.x,titik_b.y))